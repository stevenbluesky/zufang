package com.ant.vo;

import java.util.List;


/**
 * 平台网关vo
 * @author aijian
 *
 */
public class PtGatewayVo {
	
	/**转发器ID */
	private String deviceid;
	
	/**转发器名称*/
	private String name;

	/**发起操作的管理员账号*/
	private String loginname;
	
	/*0:离线
	1：在线*/
	private int gatewayonline;
	
	/**设备列表*/
	private List<PtDeviceVo> zwavedevice;

	public String getDeviceid() {
		return deviceid;
	}

	public void setDeviceid(String deviceid) {
		this.deviceid = deviceid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLoginname() {
		return loginname;
	}

	public void setLoginname(String loginname) {
		this.loginname = loginname;
	}

	public List<PtDeviceVo> getZwavedevice() {
		return zwavedevice;
	}

	public void setZwavedevice(List<PtDeviceVo> zwavedevice) {
		this.zwavedevice = zwavedevice;
	}

	public int getGatewayonline() {
		return gatewayonline;
	}

	public void setGatewayonline(int gatewayonline) {
		this.gatewayonline = gatewayonline;
	}
	
}
